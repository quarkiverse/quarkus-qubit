    private static EntityManagerFactory emf;
    private static QueryExecutorClassGenerator generator;
    private EntityManager em;

    @BeforeAll
    static void setupJpa() {
        Configuration config = new Configuration();
        config.setProperty("hibernate.connection.driver_class", "org.h2.Driver");
        config.setProperty("hibernate.connection.url", "jdbc:h2:mem:testdb;DB_CLOSE_DELAY=-1");
        config.setProperty("hibernate.connection.username", "sa");
        config.setProperty("hibernate.connection.password", "");
        config.setProperty("hibernate.hbm2ddl.auto", "create-drop");
        config.setProperty("hibernate.show_sql", "false");
        config.addAnnotatedClass(TestPerson.class);

        // SessionFactory extends EntityManagerFactory in Hibernate
        emf = config.buildSessionFactory();

        // Clear the static bytecode cache to avoid cross-test interference
        QueryExecutorClassGenerator.clearCache();
        generator = new QueryExecutorClassGenerator();

        // Insert test data
        EntityManager setupEm = emf.createEntityManager();
        setupEm.getTransaction().begin();
        setupEm.persist(new TestPerson("Alice", 30, "Engineering", 85000.0, true));
        setupEm.persist(new TestPerson("Bob", 25, "Marketing", 65000.0, true));
        setupEm.persist(new TestPerson("Charlie", 45, "Engineering", 95000.0, false));
        setupEm.persist(new TestPerson("Diana", 35, "Marketing", 75000.0, true));
        setupEm.getTransaction().commit();
        setupEm.close();
    }

    @BeforeEach
    void setupEntityManager() {
        em = emf.createEntityManager();
    }

    @AfterEach
    void closeEntityManager() {
        if (em != null && em.isOpen()) {
            em.close();
        }
    }

    @AfterAll
    static void tearDown() {
        if (emf != null) {
            emf.close();
        }
    }

