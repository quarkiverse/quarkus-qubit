        LambdaExpression predicate = BinaryOp.and(
                BinaryOp.gt(
                        new FieldAccess("age", int.class),
                        new CapturedVariable(0, int.class)),
                BinaryOp.lt(
                        new FieldAccess("salary", double.class),
                        new CapturedVariable(1, double.class)));

        byte[] bytecode = generator.generateQueryExecutorClass(
                predicate, null, List.of(), null, null,
                "io.test.Exec16", false, false);

        // age > 30 AND salary < 80000.0 -> Diana(35, 75000.0)
        List<?> results = executeGeneratedQuery(bytecode, "io.test.Exec16",
                new Object[] { 30, 80000.0 }, null, null, null);
        assertThat(results).hasSize(1);
        assertThat(((TestPerson) results.get(0)).name).isEqualTo("Diana");
